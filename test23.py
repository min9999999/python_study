while True:
    number = int(input("Enter a number: "))
    if number % 2 == 0:
        print(f"{number} 은 2로 나누어 떨어지는 숫자 입니다.")
    else:
        print(f"{number} 은 2로 나누어 떨어지지 않는 숫자 입니다.")
    if number % 3 == 0:
        print(f"{number} 은 3으로 나누어 떨어지는 숫자 입니다.")
    else:
        print(f"{number} 은 3으로 나누어 떨어지지 않는 숫자 입니다.")
    if number % 4 == 0:
        print(f"{number} 은 4로 나누어 떨어지는 숫자 입니다.")
    else:
        print(f"{number} 은 4로 나누어 떨어지지 않는 숫자 입니다.")
    if number % 5 == 0:
        print(f"{number} 은 5로 나누어 떨어지는 숫자 입니다.")
    else:
        print(f"{number} 은 5로 나누어 떨어지지 않는 숫자 입니다.")                

